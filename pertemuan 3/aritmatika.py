x = 6
y = 4

# penjumalahan
print("penjumlahan =", x + y)

# pengurangan
print("pengurangan =", x - y)

# perkalian
print("perkalian =", x * y)

#pembagian
print("permbagian =", x / y)

#modulus
print("modulus =", x % y)

#perpangkatan
print("perpangkatan =", x ** y)

#pembagian bulat
print("pembagian bulat =", x // y)