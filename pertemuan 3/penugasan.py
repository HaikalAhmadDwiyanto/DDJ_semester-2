x = 6

# penjumlahan
x += 4
print("penjumlahan =",x)

# pengurangan
x -= 4
print("penguangan =",x)

# perkalian
x *= 4
print("perkalian =", x)

# pembagian 
x /= 4
print("pembagian =", x)

# sisa bagi
x %= 4
print("sisa bagi =", x)

# pemangkatan
x **= 4
print("pemangkatan =", x)
